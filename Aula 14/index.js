
let grade = "A";

switch (grade){
    case 'A':
        console.log("Voce foi muito bem !");
        break;
    case 'B':
        console.log("Voce foi bem");
        break;
    case 'C':
        console.log("Voce foi mais ou menos bem");
        break
    case 'D':
        console.log("Voce passou ..... raspando");
        break;
    case 'F' :
        console.log("VOCE REPROVOU !!");
        break;
    default:
        console.log(grade, "NAO E UMA LETRA DE NOTA");
        
}